    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        if(jTableCadastro.getSelectedRow()!= -1){
            jTableCadastro.setValueAt(txtnome.getText(), jTableCadastro.getSelectedRow(), 0);
            jTableCadastro.setValueAt(txtSalarioBase.getText(), jTableCadastro.getSelectedRow(), 1);
            jTableCadastro.setValueAt(txtCargo.getText(), jTableCadastro.getSelectedRow(), 2);
            jTableCadastro.setValueAt(txtSalarioTotal.getText(), jTableCadastro.getSelectedRow(), 3);
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    
    private void btnCriarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarActionPerformed
        DefaultTableModel tabela1= (DefaultTableModel)jTableCadastro.getModel() ;//criando a tabela
        txtCargo.getText().toLowerCase();
        if (txtCargo.getText().equals("gestor")) {
            JOptionPane.showMessageDialog(null, "entrei no metodo");
            addiconaGestor();
          this.dados = new Object[]{txtnome.getText(), txtSalarioBase.getText(), txtCargo.getText(), txtSalarioTotal.getText()};
             tabela1.addRow(dados);
        }else if (txtCargo.getText().equals("director")) {
            JOptionPane.showMessageDialog(null, "entrei no metodo director");
            addiconaDiretor();
             this.dados = new Object[]{txtnome.getText(), txtSalarioBase.getText(), txtCargo.getText(), txtSalarioTotal.getText()};
///cria um objecto que vai armazenar as linhas da tabela
            tabela1.addRow(dados);//adicionando a linha na tabela
        }else{
            this.dados = new Object[]{txtnome.getText(), txtSalarioBase.getText(), txtCargo.getText(), txtSalarioTotal.getText()};
///cria um objecto que vai armazenar as linhas da tabela
            tabela1.addRow(dados);//adicionando a linha na tabela
        }
  
  
    }//GEN-LAST:event_btnCriarActionPerformed

