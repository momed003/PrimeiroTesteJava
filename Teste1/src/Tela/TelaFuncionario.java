    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (jTableCadastro.getSelectedRow()!=-1) {//se a linha for selecionada
            DefaultTableModel tabela1=(DefaultTableModel) jTableCadastro.getModel();
            tabela1.removeRow(jTableCadastro.getSelectedRow());
           
        }else{
             JOptionPane.showMessageDialog(null, "selecione uma lista");
        }
                
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void jTableCadastroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableCadastroMouseClicked
      if(jTableCadastro.getSelectedRow()!= -1){
          txtnome.setText(jTableCadastro.getValueAt(jTableCadastro.getSelectedRow(), 0).toString());
          txtSalarioBase.setText(jTableCadastro.getValueAt(jTableCadastro.getSelectedRow(), 1).toString());
          txtCargo.setText(jTableCadastro.getValueAt(jTableCadastro.getSelectedRow(), 2).toString());
          txtSalarioTotal.setText(jTableCadastro.getValueAt(jTableCadastro.getSelectedRow(), 3).toString());
      }
    }//GEN-LAST:event_jTableCadastroMouseClicked

